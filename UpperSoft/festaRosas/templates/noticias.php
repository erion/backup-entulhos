			FESTA DAS ROSAS 2007 TEVE PÚBLICO DE 100 MIL PESSOAS 
		</div>
		<p>
			Cerca de 100 mil pessoas passaram pelo Parque do Imigrante durante a 24ª Festa das Rosas de Sapiranga, realizada de 9 a 18 de novembro. O público pode conferir apresentações de 50 bandas de diferentes estilos musicais, além de uma infinidade de atrações como danças típicas, teatro e vídeo. 
		</p>
		<p>
			Tudo isso em três palcos: o Popular, Arena de Shows e Multiculturas. Na Festa das Rosas teve também competições esportivas como campeonato de motocross e asa-delta. Nas feiras da indústria, comércio e gastronomia foram 100 expositores divididos em dois pavilhões e em duas praças de alimentação. 
		</p>
		<p>
			Segundo o coordenador geral da Festa das Rosas, Deoclécio Grippa da Silva, o evento atingiu seus objetivos, principalmente na diversidade cultural e no dinamismo, haja visto, o curto espaço de tempo para a sua organização. No ano que vem a Festa das Rosas será realizada de 7 a 16 de novembro. 
		</p>
