			DIVERSIDADE MARCA FESTA DAS ROSAS 2008
		</div>
		<p>
			A 25ª Festa das Rosas promete diversidade em shows, atrações e diversão. A programação cultural desfila por vários estilos musicais. Além disso, a possibilidade de fazer bons negócios atrai tanto o empresariado, quanto o público consumidor, haja visto que em 2007 a festa movimentou cerca de 100 mil pessoas. Neste ano, a Festa acontece de 7 a 16 de novembro no Parque do Imigrante.
		</p>
		<p>
			O Parque possui oito hectares, sendo três de mata nativa e fica no centro da cidade de Sapiranga, de fácil acesso pela RS-239. Lá o público poderá encontrar artesanato, floriculturas, lojas de calçados, revenda de automóveis e motos, lancherias, produtos coloniais, parque de diversões, além do tradicional chope de rosas. Ao todo, serão oito ilhas de chope espalhadas pelo parque.
		</p>
		<p>
			Serão três palcos para shows: Palco Popular, Espaço Multiculturas e Arena de shows. Espaço com 87 estandes para o setor de indústria e comércio, onde estará a mini-fábrica de calçados, que produz modelos em uma hora para venda ao consumidor. Espaço para o vôo livre, praça de alimentação com 2,5 mil metros quadrados, pista para desfile de carros alegóricos, estandes de rosas e de floriculturas.
		</p>
		<p>
			Também haverá espaços institucionais para sediar estandes da Prefeitura de Sapiranga, Secretaria de Cultura do Estado, Câmara de Vereadores e Brigada Militar, entre outros órgãos e entidades.		
		</p>
		<p>
			Durante os dias da festa é oferecido um City Tour pelos principais pontos turísticos da cidade, possibilitando que os visitantes conheçam e desfrutem das belezas de Sapiranga com o acompanhamento de gente capacitada para o turismo.		
		</p>
		<p>
			Na frente do Parque, na avenida Mauá, localiza-se o espaço destinado a estacionamento de carros, motos e bicicletas. Tudo sob a segurança da Brigada Militar, que garante o bem-estar da comunidade.		
		</p>
