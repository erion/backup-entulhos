			MINI-FÁBRICA DE CALÇADOS PRODUZ MODELO NA HORA
		</div>
		<p>
			Se os visitantes da 24ª Festa das Rosas encontrarem um calçado feminino ou infantil na estande da empresa Luca Cuca sem a numeração ou cores desejadas, basta solicitar a fabricação do modelo na Mini-fábrica localizada bem ao lado da exposição dos calçados. É isso mesmo, conforme explica o gerente comercial Selmar Simon. “Em no máximo uma hora nosso cliente já pode retornar e buscar a sua encomenda”, salienta.
		</p>
		<p>
			A empresária de Porto Alegre, Ada Loeci da Rosa Giacomelli, 53 anos, foi uma das clientes que optou por encomendar sua sandália. “Escolhi este modelo pelo conforto e pela excelente qualidade. Achei ótimo ter a possibilidade de tê-lo produzido em pouco tempo na cor que mais gostei”, declarou. Ada veio de Porto Alegre com a família para visitar Sapiranga.
		</p>
		<p>
			Conforme o prefeito de Sapiranga, Nelson Spolaor, a intenção da Mini-fábrica durante a Festa das Rosas é mostrar para todos os visitantes a produção dos calçados, a qualidade e conforto dos produtos que são produzidos em Sapiranga.
		</p>
		<p>
			Na feira, os calçados estão sendo comercializados com 50% do preço de mercado. São diversos modelos femininos, de salto alto e rasteiras, além da linha infantil. A psicóloga Simone Beatriz da Silva, 40 anos, aproveitou para comprar uma sandália. “Não vim determinada a comprar, mas, como o preço realmente está atrativo, não pude resistir”, confessa. Simone saiu de São Leopoldo no sábado à tarde e foi à Festa com seus filhos. Comprou uma sandália por R$ 39,00, de modelo igual ao vendido nas lojas por R$ 79,00.		
		</p>
		<p>
			<strong>PRODUÇÃO VOLUNTÁRIA</strong> - A produção de calçados na 24ª Festa das Rosas acontece das 14h às 18h no Pavilhão de Exposições do Parque do Imigrante. Diariamente são produzidos cerca de 120 pares. Os calçados chegam com corte e costura. Depois seguem para a montagem e acabamento final. O serviço está sendo feito por uma equipe de 12 pessoas que trabalham de forma voluntária. Um percentual da venda total da feira será destinado ao Hospital de Sapiranga. “É importante ter o apoio de todos em prol de uma causa social”, enfatiza Simon.		
		</p>
