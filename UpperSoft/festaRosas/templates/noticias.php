			ELEITA CORTE DA 25ª FESTA DAS ROSAS
		</div>
		<p>
			Foi eleita a corte da 25ª Festa das Rosas no sábado (30/08) no Palácio dos Esportes. A 2ª princesa é Priscila Roberta Bonaldo, 21 anos, 1ª princesa é Aline da Silva, 18 anos, e a rainha da 25ª Festa das Rosas é Bárbara Cristina Cassel Petry, 17. A Festa será realizada de 7 a 16 de novembro no Parque do Imigrante.
		</p>
		<p>
			As candidatas desfilaram após produção feita no palco por atores do grupo de Teatro Arena. Integraram o jurado o mister Rio Grande do Sul 2009 – Vinícius de Oliveira, estilista - Joel Hartz, produtor de eventos - Jean Soares, top model da Elite - Ricardo Figueiró, musa da Festa do Sapato de Campo Bom - Camila Bloss Ribeiro, professora de etiqueta – Celeste Marquart e Bárbara Kautsmann – relações públicas do Grupo Sinos. 
		</p>
		<p>
			As jovens sapiranguenses receberam curso de postura, comportamento, etiqueta e passarela, tudo sob coordenação da estilista de moda Louize Wasem. A novidade do baile de escolha da corte neste ano foi a apresentação das candidatas em Linguagem Brasileira de Sinais (Libras), com a professora da Associação de Pais e Amigos do Deficiente Auditivo (APADA), Alessandra Berg. O baile contou com animação da Banda Sollus.
		</p>
